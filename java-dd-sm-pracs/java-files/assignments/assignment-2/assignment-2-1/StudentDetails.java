package Admission;

public class StudentDetails {
    private String name;
    private int roll;
    private String dept;
    private int year;
    private int marks;

    public StudentDetails(String name, int roll, String dept, int year, int marks) {
        this.name = name;
        this.roll = roll;
        this.dept = dept;
        this.year = year;
        this.marks = marks;
    }

}